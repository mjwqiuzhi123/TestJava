package com.mjw.redis.test;

import java.util.Iterator;
import java.util.List;
import java.util.Map;
import java.util.Set;

import redis.clients.jedis.Jedis;

public class TestRedis {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		test4();
	}

	// 建立连接，ping服务器
	public static void test() {
		// 连接本地的 Redis 服务
		Jedis jedis = new Jedis("localhost");
		System.out.println("连接成功");
		// 查看服务是否运行
		System.out.println("服务正在运行: " + jedis.ping());
	}

	// 建立连接，设置字符串String
	public static void test1() {
		// 连接本地的 Redis 服务
		Jedis jedis = new Jedis("localhost");
		System.out.println("连接成功");
		// 设置 redis 字符串数据
		jedis.set("runoobkey", "www.runoob.com");
		// 获取存储的数据并输出
		System.out.println("redis 存储的字符串为: " + jedis.get("runoobkey"));
	}

	// 设置list
	public static void test2() {
		// 连接本地的 Redis 服务
		Jedis jedis = new Jedis("localhost");
		System.out.println("连接成功");
		// 存储数据到列表中
		jedis.lpush("site-list", "Runoob");
		jedis.lpush("site-list", "Google");
		jedis.lpush("site-list", "Taobao");
		// 获取存储的数据并输出
		List<String> list = jedis.lrange("site-list", 0, 2);
		for (int i = 0; i < list.size(); i++) {
			System.out.println("列表项为: " + list.get(i));
		}
	}

	// 获取keys
	public static void test3() {
		// 连接本地的 Redis 服务
		Jedis jedis = new Jedis("localhost");
		System.out.println("连接成功");

		// 获取数据并输出
		Set<String> keys = jedis.keys("*");
		Iterator<String> it = keys.iterator();
		while (it.hasNext()) {
			String key = it.next();
			System.out.println(key);
		}
	}
	
	// 添加hash
	public static void test4(){
		// 连接redis
		Jedis jedis = new Jedis("localhost");
		System.out.println("连接redis成功");
		
		// 添加数据
		jedis.hset("hashTest", "field1", "test1");
		jedis.hset("hashTest", "field2", "test2");
		jedis.hset("hashTest", "field3", "test3");
		
		// 获取数据
		Map<String, String> map = jedis.hgetAll("hashTest");
		Iterator<String> it = map.keySet().iterator();
		while(it.hasNext()){
			String filed = it.next();
			System.out.println("field: " + filed + " value: " + map.get(filed));
		}
	}
}
